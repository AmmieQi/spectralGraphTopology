library(extrafont)
library(latex2exp)
ratios <- c(5, 10, 30, 100, 250, 500, 1000)
n_ratios <- c(1:length(ratios))

rel_err_naive <- readRDS(file = "block-rel-err-naive.rds")
fscore_naive  <- readRDS(file = "block-fscore-naive.rds")
rel_err_qp    <- readRDS(file = "block-rel-err-QP.rds")
fscore_qp     <- readRDS(file = "block-fscore-QP.rds")
rel_err_spec  <- readRDS(file = "block-rel-err-SGL.rds")
fscore_spec   <- readRDS(file = "block-fscore-SGL.rds")

colors <- c("#0B032D", "#843B62", "#6ABA81")
pch <- c(15, 7, 9)
lty <- c(1, 2, 6)
legend <- c("Naive", "QP", "SGL (proposed)")
xlab <- TeX("$\\mathit{p} / \\mathit{n}$")
gr = .5 * (1 + sqrt(5))
setEPS()
postscript("../latex/figures/block_relative_error_bipartite.ps", family = "ComputerModern", height = 5, width = gr * 3.5)
plot(n_ratios, rel_err_naive, type = "b", lty = lty[1], pch=pch[1], cex=.75, ylim=c(0, .9),
     xlab = xlab, ylab = "Average Relative Error", col = colors[1], xaxt = "n")
grid()
lines(n_ratios, rel_err_qp,   type = "b", lty=lty[2], pch=pch[2], cex=.75, col = colors[2], xaxt = "n")
lines(n_ratios, rel_err_spec, type = "b", lty=lty[3], pch=pch[3], cex=.75, col = colors[3], xaxt = "n")
axis(side = 1, at = n_ratios, labels = ratios)
legend("topright", legend=legend, col=colors, pch=pch, lty=lty, bty="n")
dev.off()
embed_fonts("../latex/figures/block_relative_error_bipartite.ps", outfile="../latex/figures/block_relative_error_bipartite.ps")
setEPS()
postscript("../latex/figures/block_fscore_bipartite.ps", family = "ComputerModern", height = 5, width = gr * 3.5)
plot(n_ratios, fscore_naive, ylim=c(.5, 1.), xlab = xlab, ylab = "Average F-score", type = "b",
     pch=pch[1], lty=lty[1], cex=.75, col = colors[1], xaxt = "n")
grid()
lines(n_ratios, fscore_qp,   type = "b", lty=lty[2], pch=pch[2], cex=.75, col = colors[2], xaxt = "n")
lines(n_ratios, fscore_spec, type = "b", lty=lty[3], pch=pch[3], cex=.75, col = colors[3], xaxt = "n")
axis(side = 1, at = n_ratios, labels = ratios)
legend("bottomright", legend=legend, col=colors, pch=pch, lty=lty, bty="n")
dev.off()
embed_fonts("../latex/figures/block_fscore_bipartite.ps", outfile="../latex/figures/block_fscore_bipartite.ps")
